hyttiluokka = input("Mika on hyttiluokkasi?: ")

if hyttiluokka == "LUX":
    print("LUX on parvekkeellinen hytti")
elif hyttiluokka == "A":
    print("A on ikkunallinen hytti autokannen ylapuolella")
elif hyttiluokka == "B":
    print("B on ikkunanaton hytti autokannen ylapuolella")
elif hyttiluokka == "C":
    print("C on ikkunaton hytti autokannen alapuolella")
else:
    print("Virheellinen hyttiluokka.")
